    public void llenarTabla(){ 
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            jTableSuscritos.setModel(modelo);
            Class.forName("org.sqlite.JDBC");//especificamos el driver
            Connection conn = DriverManager.getConnection("jdbc:sqlite:mangas.db"); //creamos la conexiÃ³n, si la BD no existe la crea
            java.sql.PreparedStatement stat = conn.prepareStatement("select * from suscritos");
            ResultSet rs = stat.executeQuery();
            ResultSetMetaData rsMd = rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();
            modelo.addColumn("Nombre");
            modelo.addColumn("Actual");
            modelo.addColumn("Descargado");
            //Creando las filas para el JTable
            while (rs.next()) {
                Object[] fila = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    fila[i]=rs.getObject(i+1);
                }
                modelo.addRow(fila);
            }
            rs.close();
            stat.close();
        } catch (ClassNotFoundException | SQLException ex) {
            //ex.printStackTrace();
        }
        TableColumn columna = jTableSuscritos.getColumn("Nombre");
        columna.setPreferredWidth(100);
        columna = jTableSuscritos.getColumn("Actual");
        columna.setPreferredWidth(20);
        columna = jTableSuscritos.getColumn("Descargado");
        columna.setPreferredWidth(20);
        
    }
