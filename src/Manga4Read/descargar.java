    public void llenarTabla(){ 
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            jTableDecargas.setModel(modelo);
            Class.forName("org.sqlite.JDBC");//especificamos el driver
            Connection conn = DriverManager.getConnection("jdbc:sqlite:mangas.db"); //creamos la conexiÃ³n, si la BD no existe la crea
            java.sql.PreparedStatement stat = conn.prepareStatement("select * from temporal");
            ResultSet rs = stat.executeQuery();
            ResultSetMetaData rsMd = rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();
            modelo.addColumn("Pag.");
            modelo.addColumn("Enlace");
            modelo.addColumn("Estado");
            
            //Creando las filas para el JTable
            while (rs.next()) {
                Object[] fila = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    fila[i]=rs.getObject(i+1);
                }
                modelo.addRow(fila);
            }
            rs.close();
            stat.close();
        } catch (ClassNotFoundException | SQLException ex) {
            //ex.printStackTrace();
        }
        TableColumn columna = jTableDecargas.getColumn("Pag.");
        columna.setPreferredWidth(20);
        columna = jTableDecargas.getColumn("Enlace");
        columna.setPreferredWidth(320);
        columna = jTableDecargas.getColumn("Estado");
        columna.setPreferredWidth(50);
        
    }
    
